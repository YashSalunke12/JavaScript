//Arithmatic Operators
let a = 6;
let b = 4;

console.log("a + b = ", a+b);
console.log("a - b = ", a-b);
console.log("a * b = ", a*b);
console.log("a / b = ", a/b);
console.log("a % b = ", a%b);
console.log("a ** b = ", a**b);
console.log("a++ = ", a++);
console.log("a-- = ", a--);

//Assignment Operator
let c = 5;
let d = 2;

console.log("+= = ", c+=d);
console.log("-= = ", c-=d);
console.log("*= = ", c*=d);
console.log("/= = ", c/=d);
console.log("%= = ", c%=d);
console.log("**= = ", c**=d);

